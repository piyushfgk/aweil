                        <h3>Sections</h3>
                        <p><b>The Beginning:</b>
                        <p>The history and development of Indian Ordnance Factories is directly linked with the British reign in India. East India company of England for their economic interest in India and to increase their political hold considered military hardware as a vital element. During 1775 British authorities accepted the establishment of the Board of Ordnance in Fort William, Kolkata. This marks the official beginning of the Army Ordnance in India.</p>
						<p>In 1787 a gunpowder factory was established at Ishapore which started production from 1791 ( at which location Rifle Factory was established in 1904). In 1801 a Gun Carriage Agency at Cossipore, Kolkata (presently known as Gun & Shell Factory, Cossipore) was established and production started from 18th March, 1802. This is the first Industrial establishment of Ordnance Factories which has continued its existence till date.</p>
