<p>भारतीय आयुध कारखानों के हथियार, वाहन और उपकरण (डब्ल्यूवी एंड ई) डिवीजन के 8 कारखानों में फील्ड गन फैक्ट्री सबसे कम उम्र की फैक्ट्री है। यह कानपुर, यूपी में स्थित है। 10 किमी की दूरी पर। कानपुर सेंट्रल रेलवे स्टेशन से।</p>

<p>1965 के भारत-पाक युद्ध और 1971 के बांग्लादेश मुक्ति युद्ध के मद्देनजर, हथियार उपकरण, विशेष रूप से आर्टिलरी फील्ड गन की आवश्यकता बहुत अधिक थी। भारत सरकार द्वारा फील्ड गन उत्पादन के लिए समर्पित एक नया कारखाना स्थापित करने का निर्णय लिया गया, जो फील्ड गन के उत्पादन के लिए एक स्व-निहित इकाई होना चाहिए। कारखाना बड़े कैलिबर आयुधों के निर्माण के लिए आवश्यक सभी सुविधाओं की पूर्ति करता है। इसमें स्टील के वांछित ग्रेड की तैयारी, हीट ट्रीटमेंट, मशीनिंग, निरीक्षण, फाइनल असेंबली, प्रूफ फायर और इश्यू शामिल हैं। फील्ड गन फैक्ट्री में फैक्ट्री के अंदर उत्पाद की आसान और तार्किक आवाजाही सुनिश्चित करने के लिए मैन्युफैक्चरिंग ऑपरेशन के अनुसार डिज़ाइन किए गए शॉप लेआउट का एक अच्छा एर्गोनॉमिक्स है।</p>

<p>फैक्ट्री 104.10 हेक्टेयर के क्षेत्र में फैली हुई है, 39% क्षेत्र दुकानों और विविध भवनों से आच्छादित है, और शेष 61% को हरा-भरा रखा गया है। बुनियादी ढांचे को वैज्ञानिक रूप से डिजाइन किया गया है और इसमें औद्योगिक / वाणिज्यिक भवन और सेवाएं शामिल हैं जैसे बिजली आपूर्ति, जल आपूर्ति, सीवेज, एयर कंडीशनिंग, जल निकासी प्रणाली, संपीड़ित वायु आपूर्ति आदि।</p>

<p>कारखाना उत्पादन सुविधाओं को मोटे तौर पर धातुकर्म और इंजीनियरिंग क्षेत्र में वर्गीकृत किया जा सकता है।</p>

<p>फील्ड गन फैक्ट्री, कानपुर अपने पदवी का पर्याय है, देश की रक्षा जरूरतों के अनुरूप विभिन्न प्रकार की फील्ड गनों के निर्माण की एक अनूठी क्षमता है। कारखाने में सभी प्रकार के नवीनतम और परिष्कृत उत्पादन प्रसंस्करण उपकरणों के साथ पिघलने, फोर्जिंग से लेकर गुणवत्ता में उत्कृष्टता की एक अंतर्निहित उत्कृष्ट डिग्री के साथ मशीनिंग खत्म करने के लिए एक सेट अप है।</p>

<p>इसके विविधीकरण कार्यक्रम के हिस्से के रूप में, कारखाने की उत्पाद श्रृंखला का और विस्तार किया गया है और यह कारखाना सरकारी और निजी क्षेत्रों में नागरिक बाजार की मांगों को पूरा कर रहा है और व्यक्तिगत हथियारों को भी पूरा कर रहा है।</p>

<p>कारखाना उच्चतम अंतरराष्ट्रीय गुणवत्ता के स्टोर बनाने में विश्वास करता है जो इस तथ्य से प्रकट होता है कि कारखाने ने अपनी उत्पादन गतिविधियों के सभी क्षेत्रों में अंतर्राष्ट्रीय गुणवत्ता प्रबंधन प्रणाली मानक (आईएसओ 9001: 2015) को लागू किया है। कारखाने के पास आईएसओ 14000: 2015 और ओएचएसएएस 18000: 2007 प्रमाणन है। कारखाने में एनएबीएल से मान्यता प्राप्त प्रयोगशाला भी है। यह सुनिश्चित करने के लिए कि उत्पादन गतिविधियों में उपयोग किए जाने वाले सभी माप उपकरणों और उपकरणों को अंतर्राष्ट्रीय मानकों के अनुसार कैलिब्रेट किया गया है।
</p>
