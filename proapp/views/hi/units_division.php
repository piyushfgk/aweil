<p>आधुनिक सैन्य हार्डवेयर में विकास के अनुरूप उन्नत हथियार प्रणालियों के एक निर्माता के रूप में गन कैरिज एजेंसी के बैल-चालक आविष्कारों से परिवर्तन एक दिलचस्प कहानी है। अब यह पूरी तरह से विकसित इंजीनियरिंग फैक्ट्री है जो सेना, नौसेना और अन्य नागरिक क्षेत्रों के लिए गोला-बारूद हार्डवेयर और आयुध भंडार के निर्माण में लगी हुई है। ARMY कवर मीडियम कैलिबर गन, L-70 एंटी-एयरक्राफ्ट गन 84mm के लिए मोर्टार एलिवेटिंग मास, रॉकेट लॉन्चर, 30 से 125 mm तक के मीडियम कैलिबर के गोले, विभिन्न प्रकार के फ़्यूज़ और प्राइमर।</p>

<p><b>मिशन</b></p>

<p>अत्याधुनिक युद्धक्षेत्र उपकरणों का उत्पादन।</p>

<p><b>नज़र</b></p>

<p>हमारे सशस्त्र बलों को आधुनिक "रक्षा और युद्धक्षेत्र उपकरण" से लैस करना।</p>
<p>उत्पादन सुविधाओं का निरंतर आधुनिकीकरण करना।</p>
<p>कर्मियों को प्रशिक्षित और प्रेरित करना।</p>
<p>अधिग्रहण, तालमेल और अनुसंधान एवं विकास गतिविधियों के माध्यम से खुद को प्रौद्योगिकियों से लैस करना।</p>
<p>गुणवत्ता में लगातार सुधार करने के लिए।</p>
<p>ग्राहकों की संतुष्टि के उच्चतम स्तर को प्राप्त करने के लिए।</p>


<p><b>इतिहास और विकास</b></p>

<p>कारखाने के ऐतिहासिक स्थलचिह्न और विकास इस प्रकार हैं:</p>

<p><b>1801</b>: फ़ैक्टरी बनाने के लिए खरीदी गई ज़मीन</p>
<p><b>1802</b>: (18 मार्च) गन कैरिज के निर्माण और मरम्मत के लिए गन कैरिज एजेंसी की स्थापना की।</p>
<p><b>1830</b>: फोर्ट विलियम से गन फाउंड्री का स्थानांतरण और गन कैरिज एजेंसी का उन्मूलन और कारखाने का नाम बदलकर गन फाउंड्री करना।</p>
<p><b>1832</b>: बैल चालित मशीनों से भाप से चलने वाली मशीनों में परिवर्तन।</p>
<p><b>1840</b>: पीतल और लोहे की तोपों का उत्पादन स्थापित।</p>
<p><b>1872</b>: शैल उत्पादन की स्थापना और फाउंड्री और शैल फैक्टरी के रूप में नामकरण</p>
<p><b>1880</b>: फ्यूज सेक्शन की स्थापना।</p>
<p><b>1892</b>: भारत में पहली बार थोक में गुणवत्तापूर्ण स्टील का उत्पादन शुरू हुआ।</p>
<p><b>1896 </b>: रोलिंग मिल की स्थापना।</p>
<p><b>1905</b>: शिफ्टिंग फर्नेस और रोलिंग मिल को ईशापुर में स्थानांतरित कर दिया गया। गन एंड शेल फैक्ट्री के रूप में पुनर्नामित।</p>
<p><b>1908</b>: भाप से बिजली से चलने वाली मशीनरी में बदलाव</p>
<p><b>1927</b>: अपरेंटिस योजना शुरू हुई।</p>
<p><b>1939</b>: द्वितीय विश्व युद्ध का प्रकोप; दम दम कार्यशाला को गन एंड शेल फैक्ट्री, कोसीपोर से जोड़ना।</p>
<p><b>1958</b>: ट्रैक्टर परियोजना शुरू हुई।</p>
<p><b>1962</b>: विमान भेदी तोपों का उत्पादन शुरू हुआ।</p>
<p><b>1964</b>: दम दम एक स्वतंत्र इकाई के रूप में स्थापित किया गया था।</p>
<p><b>1965</b>: ट्रैक्टर परियोजना बीईएमएल को हस्तांतरित।</p>
<p><b>1998</b>: 23 मिमी प्रेशर बैरल, 30 मिमी बैलिस्टिक बैरल, 9 मिमी सब कैलिबर बैरल और AK-230 गनकंपोनेंट्स (नौसेना) की स्थापना।</p>
<p><b>1999</b>: 12.7 मिमी वायु रक्षा गन और 0.32 की स्थापना? पिस्तौल।</p>
<p><b>2002</b>: फैक्ट्री के 200 साल पूरे होने पर, आयुध संगठन के भीतर आईएसओ 9001-2000 प्रमाणन प्राप्त करने वाली पहली फैक्ट्री।</p>
<p><b>2003</b>: शेल 30mm के उत्पादन की स्थापना। एचई/टी, 51 मिमी मोर्टार।</p>
<p><b>2004</b>: बम 81 मिमी एचई, बम 51 मिमी एचई और 40 मिमी के उत्पादन की स्थापना। पीएफएफसी शेल।</p>

