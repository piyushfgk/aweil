<p>स्वतंत्रता के बाद, 1949 में कारखाने का नाम बदलकर स्मॉल आर्म्स फैक्ट्री कर दिया गया। कारखाने से निकलने वाला पहला हथियार प्रसिद्ध 0.303” ब्रेन गन था, जिसे बाद में वर्ष 1964 में 7.62 मिमी लाइट मशीन गन में विकसित किया गया था। 1963 में क्लोज कॉम्बैट में अपनी उपयोगिता के लिए प्रसिद्ध 9एमएम कार्बाइन का उत्पादन शुरू किया गया था जो आज भी जारी है। कारखाने द्वारा उत्पादित अन्य उल्लेखनीय हथियार 1966 में 2-इंच मोर्टार और 1984 में इसका नया संस्करण 51 मिमी मोर्टार है। बेल्जियम के मैसर्स एफएन के सहयोग से मध्यम मशीन गन एमएजी 7.62 मिमी का उत्पादन 1976 में शुरू किया गया था और आज तक जारी है। 0.32” रिवाल्वर एसएएफ का एक अन्य प्रतिष्ठित उत्पाद है जिसकी सिविल ट्रेड मार्केट में अपनी पहचान है। 2021 में, नागरिक ग्राहकों के लिए 0.32” रिवाल्वर का एक नया संस्करण “PRAHAR” नाम से लॉन्च किया गया है।</p>

