							<h3>Company <span>overview</span></h3>
							<p>Advanced Weapons & Equipment India Ltd. (AW&EIL) is one of the seven (7) new Defence PSUs formed by converting the Ordnance Factory Board into  fully Government owned Enterprises. AW&EIL has been incorporated under the Companies Act 2013 on 14th August, 2021 however, its commencement of business took place on 1st October 2021 notified by DDP. It comprises of the following production and non- production units:</p>
							<p>Rifle Factory Ishapore, Small Arms Factory, Kanpur, Gun & Shell Factory, Cossipore, Ordnance Factory Tiruchirapalli, Ordnance Factory, Kanpur, Field Gun Factory, Kanpur, Gun Carriage Factory, Jabalpur, and Ordnance Factory Project Korwa and non- production  units  at  Ordnance  Factories  Institute  of  Learning Ishapore and Regional Controllerate of Safety, Kanpur.</p>
