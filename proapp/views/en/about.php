							<h3>Company <span>History</span></h3>
							<p><b>The Beginning:</b><p>
							<p>The history and development of Indian Ordnance Factories is directly linked with the British reign in India. East India company of England for their economic interest in India and to increase their political hold considered military hardware as a vital element. During 1775 British authorities accepted the establishment of the Board of Ordnance in Fort William, Kolkata. This marks the official beginning of the Army Ordnance in India.</p>
							<p>In 1787 a gunpowder factory was established at Ishapore which started production from 1791 ( at which location Rifle Factory was established in 1904). In 1801 a Gun Carriage Agency at Cossipore, Kolkata (presently known as Gun & Shell Factory, Cossipore) was established and production started from 18th March, 1802. This is the first Industrial establishment of Ordnance Factories which has continued its existence till date.</p>
							<h3>Evolution of <span>Indian Ordnance Factories</span></h3>
							<p>The growth of the Ordnance Factories leading to its present setup has been continuous but in spurts. There were 18 ordnance factories before India became independent in 1947. 21 factories have been established after independence - mostly, in wake of defence preparedness imperatives caused by the three major wars fought by the Indian Armed forces. 40th Factory is under establishment at Nalanda, Bihar.
							Main Events
							</p>
							<p>
							<b>Main events in the evolution of Ordnance Factory can be listed as below:</b></p>
							<p><b>1801</b> - Establishment of Gun Carriage Agency at Cossipore, Kolkata.</p>
							<p><b>1802</b> - Production started from 18th March 1802 at Cossipore.</p>
							<p><b>1906</b> - The Administration of Indian Ordnance Factories came under a separate charge as 'IG of Ordnance Factories'.</p>
							<p><b>1933</b> - Charged to 'Director of Ordnance Factories'.</p>
							<p><b>1948</b> - Placed under direct control of the Ministry of Defence.</p>
							<p><b>1962</b> - Dept. of Defence Production was set up at the Ministry of Defence.</p>
							<p><b>1979</b> - Ordnance Factory Board came into existence from 2nd April.</p>
							<p><b>2021</b> - AWEIL came into existence from 1 Oct, 2021 post corporatisation of OFB</p>
