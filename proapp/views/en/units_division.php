<p>Small Arms Factory (SAF) Kanpur is a premier weapons production unit of Advanced Weapons and Equipment India Limited under the Ministry of Defence with a proud history of more than 75 years in design, development and manufacturing of weapons for Armed Forces, Central Paramilitary Forces and State Police.  It also manufactures various models of Revolver 0.32” to meet varying needs of our esteemed customers in the civilian market.</p>
<p>SAF was built in 1942 at Kanpur as  part of  the strategic plan to have a shadow factory of Rifle Factory Ishapore to undertake manufacture of Small Arms as a sequel to threat on the eastern border during World War-II and it was named as Transplantation Project-1.</p>

<p>After independence, the factory was renamed as Small Arms Factory in 1949.The first weapon rolling out from the factory was the famous 0.303” Bren Gun, which was later developed into 7.62mm Light Machine Gun in the year 1964. In 1963, production of the 9mm Carbine, famous for its utility in Close Combat, was started which continues to this day. Other notable weapons produced by the factory are 2-inch Mortar in 1966 and its new version 51mm Mortar in 1984. Production of Medium Machine Gun MAG 7.62mm in collaboration with M/s FN of Belgium was started in 1976 and continues till date. 0.32” Revolver is another prestigious product of SAF having its mark in the Civil Trade market. In 2021, a new version of the 0.32” revolver named “PRAHAAR” has been launched for civilian customers.</p>

<p>The factory entered an era of modern weapon production by taking up the production of the INSAS family of weapons which were jointly developed with DRDO/ARDE during the year 1996. Two variants of this family i.e. LMG 5.56mm & Rifle 5.56mm are under current production at SAF.</p>

<p>Weapon system JVPC 5.56mm has been successfully designed and developed by Small Arms Factory in association with ARDE Pune. The JVPC 5.56mm weapon has been supplied to various CAPFs and SPOs.  LMG 7.62mm Belt fed and Side Swing Revolver 0.32” are under various stages of development/user trials.</p>

<p>The factory is accredited with Integrated Management System Manual (IMS) Certification covering Quality Management System (IS/ISO 9001:2015), Environmental Management System (IS/ISO:14001:2015), Occupational Health and Safety Management System (IS 45001:2018) and Energy Management System(IS/ISO:50001:2011).</p>
