<p><b>1830</b>: Shifting of Gun Foundry from Fort William and abolition of Gun Carriage Agency and renaming the factory as Gun Foundry.</p>
<p><b>1832</b>: Change over from Bullock Driven machines to Steam Driven Machines.</p>
<p><b>1840</b>: Production of both Brass and Iron Guns Established.</p>
<p><b>1872</b>: Establishment of Shell Production & renaming as Foundry & Shell Factory</p>
<p><b>1880</b>: Establishment of Fuze Section.</p>
<p><b>1892</b>: Quality Steel production in bulk started first time in India.</p>
<p><b>1896 </b>: Establishment of Rolling Mill.</p>
<p><b>1905</b>: Shifting Furnaces and Rolling Mill shifted to Ishapore. Renamed as Gun & Shell Factory.</p>
<p><b>1908</b>: Changeover from Steam to Electricity driven machinery</p>
<p><b>1927</b>: Apprentice Scheme started.</p>
<p><b>1939</b>: Outbreak of World War II; Annexing of Dum Dum Workshop to Gun & Shell Factory, Cossipore.</p>
<p><b>1958</b>: Tractor Project started.</p>
<p><b>1962</b>: Production of Anti Aircraft Guns started.</p>
<p><b>1964</b>: Dum Dum was setup as an independent unit.</p>
<p><b>1965</b>: Tractor Project transferred to BEML.</p>
<p><b>1998</b>: Establishment of 23 mm Pressure Barrel, 30 mm Ballistic Barrel, 9 mm Sub Caliber Barrel and AK-230 GunComponents (Naval).</p>
<p><b>1999</b>: Establishment of 12.7 mm Air Defence Gun & 0.32? Pistol.</p>
<p><b>2002</b>: Completion of 200 years of Factory, The first Factory within the Ordnance organisation to get ISO 9001-2000 certification.</p>
<p><b>2003</b>: Establishment of production of Shell 30mm. HE/T, 51 mm Mortar.</p>
<p><b>2004</b>: Establishment of production of Bomb 81 mm HE, Bomb 51 mm HE and 40mm. PFFC Shell.</p>

