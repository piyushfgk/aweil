<p>Field Gun Factory is the youngest factory among the 8 factories of the Weapons, Vehicles and Equipment (WV&E) division of Indian Ordnance factories. It is located at Kanpur, U.P. at a distance of 10 kms. from Kanpur Central railway station.</p>

<p>In the wake of the Indo-Pak war of 1965 and the Bangladesh Liberation war of 1971, the requirement of Weapon equipment, especially artillery field guns was huge. It was decided by the Indian Government to establish a new factory dedicated to Field gun production, which should be a self-contained unit for production of Field Guns. The factory caters for all facilities required for manufacturing of large caliber ordnances. This includes preparation of desired grade of steel, heat treatment, machining, inspection, final assembly, proof fire, and issue. Field Gun Factory has a good ergonomics of shop layout designed as per manufacturing operation to ensure easy and logical movement of product inside the factory.</p>

<p>The factory spreads over an area of 104.10 Hectares, 39% of the area is covered by shops and miscellaneous buildings, and the rest 61% is maintained green. The infrastructure has been scientifically designed and consists of industrial/ commercial buildings and services viz power supply, water supply, sewage, air conditioning, drainage system, compressed air supply etc.</p>

<p>The factory production facilities can be broadly categorized into the Metallurgical and Engineering sector.</p>

<p>Field Gun Factory, Kanpur synonymous with its appellation, has a unique capacity of manufacturing various types of Field Guns commensurate with the country's Defence needs. The factory has a set up with all types of the latest and sophisticated production processing equipment right from melting, forging to finish machining with a built-in superlative degree of excellence in quality.</p>

<p>As part of its diversification programme, the product range of the factory has been further expanded and the factory is catering to the demands of the civilian market in government and private sectors and also personal weapons.</p>

<p>The factory believes in producing stores of highest international quality which is revealed from the fact that the factory has implemented the International Quality Management System standard (ISO 9001: 2015) in all spheres of its production activities. The factory possesses ISO 14000: 2015 and OHSAS 18000:2007 certification. The factory also has a NABL accredited lab. to ensure that all the measurement devices and instruments, used in production activities are calibrated to International Standards.
</p>
