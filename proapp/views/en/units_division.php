												<div class="faq-heading"  id="FaqTitle7">
													<h4 class="faq-title">
														<a class="collapsed" data-toggle="collapse" data-parent="#accordion" href="#faq7">Rifle Factory Ishapore</a>
													</h4>
												</div>
												<div id="faq7" class="panel-collapse collapse" role="tabpanel" aria-labelledby="FaqTitle4">
													<div class="faq-body"><p>Towards the end of the 18th century, the East India Company planned to set up a GunPowder Factory of their own. They selected a place where the Dutch Ostend Company had run a GunPowder Factory from 1712 to 1744. The selected land was in possession of Maharaja Nobkissen Bahadur from 1769. The East India Company made an exchange settlement with the Maharaja to acquire this land.</p>

<p>A Deed dated 28th April, 1778 was made between the Maharaja and the Governor General of United Company, Warren Hastings. In exchange for Noapara and certain other villages belonging to the estate of the Maharaja, the Company awarded him talukdari of several villages situated in Calcutta, containing bazaar Sootaloottee, Subah Bazar, Charles Bazar, Baugh Bazar etc.</p>

<p>It is known from the marble plaque on the main gate of the factory that the work for setting up the Gun Powder factory Ishapore, commenced sometime in 1787 under J. Farquhar, Agent and production started from 1st January 1791 and continued for more than a hundred years till 1st June 1902.</p>

<p>From the editorial note of the Statesman dated 8 July 1901, it is revealed that one Captain Moore of the Indian Ordnance department was assigned the task of creating facilities at Ishapore for turning out 25,000 rifles and carbines annually on the lines of an existing factory at Sparkbrook near Birmingham. The site of the gunpowder factory was selected for setting up this factory. On completion of the work, production started from 20th September 1904. The factory was renamed as Rifle Factory, Ishapore.</p>

<p>Parallels of this factory can be drawn with Royal Small Arm Factory of Enfield Lock- a suburban township, North of London, where the famous Enfield Rifle was born and which had been the home for the design, development, manufacture and testing of small arms. Ishapore was rightly christened as the 'Enfield of India' in view of the geographical and other similarities between the two locations. RFI thence embarked upon a long journey in the field of small arms and in its march to glory, produced blend of small arms from the kookriees, swords, bayonets, revolvers, pistols, carbines, muskets, bazooka launcher rockets to 7.62 SLR Ishapore Rifles, 9mm Auto Pistols and 5.56 INSAS and made its indelible mark in the history of small arms production in India.</p>

<p>During World War I & II, the factory produced large quantities of .303 Bolt Action Rifles to meet the war requirements. In addition to this, repair works for Light Machine Guns like MAXIM, LEWIS, HOTCHKISS and VICKERS were also carried out.</p>

<p>The first step towards Indianisation of Ordnance Factories Supervisory Cadre was initiated at RFI when the scheme for training of apprentices started in the year 1920. The Ordnance Technical School a pioneer in Technical Training in Ordnance Factories imparted Training to the young inductees at workmen and supervisory levels.</p>

<p>After Independence, due to shortage of work load from the Military Services, the factory had to diversify its production activities. The factory undertook design & development of Civil weapons to keep the manpower engaged. Manufacture of 12 Bore DBBL & SBBL Shot Guns started in the year 1953. In 1956, the .315" Sporting Rifle was established. Even today this weapon has a very good demand in the Civil Market.</p>

<p>In 1962, the factory was called upon to establish a 7.62mm Semi-Automatic Rifle to meet the Army's emergent operational requirements. The task was carried out with great success and the factory was commended for the extraordinary feat in the India Parliament by the then Defence Minister.' The design and development of the 7.62mm Ishapore Rifle was completed within the shortest possible time and regular production started with effect from 1964-65. In recognition of this achievement of the factory, the then General Manager K.C. Banerjee was conferred 'PADMA SHRI' by the Govt. of India.</p>

<p>Ranging! Sub-Calibre Guns for 106mm RCL, 105 Tank Gun and 84mm Rocket Launcher were successfully productionised by the factory during the seventies and early eighties. The factory successfully established production of 9mm Pistol Auto in the year 1977 which continues to be in production till date.</p>

<p>In the year 1994, serial production of the 5.56mm INSAS Rifle was started. The rifle of contemporary international standard had been developed by the factory in partnership with ARDE. DRDO Technology Assimilation Award was conferred on the factory by the Prime Minister of India in 1997.</p>

<p>In the year 1999, RFI developed and established production of a .22" Sporting Rifle of international quality for the Civil sector. Through in-house R&D, RFI has also developed 0.22 Revolver in 2001-2002 for the civil sector, where it is attracting large demand.</p>

<p>In recent times, the factory has modernized considerably with the induction of latest technology in production areas to maintain high quality standards and improved production techniques to economize cost of output. CAD/CAM has been provided for the product! process design. Today, RFI is an ISO-9000 certified company. Its Chemical & Metallurgical Laboratory is NABL accredited Now, with its 'State-of-the-Art' technology, skilled manpower and best manufacturing practices, it is considered as the Jewel in the Crown of the Ordnance Factories Organisation.</p>
